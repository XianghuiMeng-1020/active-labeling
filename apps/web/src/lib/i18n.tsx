type I18nContextType = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  labelText: (label: string) => string;
};

const I18nContext = createContext<I18nContextType | null>(null);

function defaultLocale(): Locale {
  try {
    const saved = localStorage.getItem(LOCALE_KEY) as Locale | null;
    if (saved && messages[saved]) return saved;
  } catch { /* private browsing or disabled storage */ }
  try {
    const lang = navigator.language.toLowerCase();
    if (lang.startsWith("zh-tw") || lang.startsWith("zh-hk") || lang.startsWith("zh-mo")) return "zh-Hant";
    if (lang.startsWith("zh")) return "zh-Hans";
  } catch { /* SSR fallback */ }
  return "en";
}

/** For class components (e.g. ErrorBoundary) that cannot use useI18n; reads locale from localStorage. */
export function getErrorBoundaryStrings(): { title: string; description: string; reload: string } {
  const locale = typeof localStorage !== "undefined" ? (localStorage.getItem(LOCALE_KEY) as Locale | null) : null;
  const dict = (locale && messages[locale]) ? messages[locale] : messages.en;
  return {
    title: dict["error.pageError"] ?? "Something went wrong",
    description: dict["error.pageErrorDesc"] ?? "The app encountered an unexpected error. Please refresh and try again.",
    reload: dict["error.reload"] ?? "Refresh page"
  };
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => defaultLocale());

  useEffect(() => {
    localStorage.setItem(LOCALE_KEY, locale);
    document.documentElement.lang = locale === "zh-Hans" ? "zh-CN" : locale === "zh-Hant" ? "zh-TW" : "en";
  }, [locale]);

  const value = useMemo<I18nContextType>(() => {
    const dict = messages[locale];
    const t = (key: string, vars?: Record<string, string | number>) => {
      const raw = dict[key] ?? messages.en[key] ?? key;
      if (!vars) return raw;
      return Object.entries(vars).reduce((acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)), raw);
    };
    const labelText = (label: string) => {
      const key = `label.${label}`;
      const value = t(key);
      return value === key ? label : value;
    };
    return { locale, setLocale: setLocaleState, t, labelText };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}

